ALTER view PersonDevice as
select
pei.ID,
pei.FirstName,
pei.LastName,
d.Phone,
d.Headphones,
pei.DOB
FROM PersonInfo pei
INNER JOIN Device d
	ON d.ID = pei.ID 
-- You can use = instead of like, but it wont work in this case
-- = takes thinks literally, doesnt see % as a special charecter
-- Like does and it supports special charecters
WHERE d.Headphones LIKE 'Bose%'
GO

